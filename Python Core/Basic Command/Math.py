def MinMax():
  x = min(5, 10, 25)
  y = max(5, 10, 25)

  print(x)
  print(y)

  #or
  a=(5, 10, 25)
  x = min(a)
  y = max(a)
  print(x)
  print(y)

  #pow
  x = pow(4, 3)
  print(x) #64

  #abs()
  x = abs(-7.25) #The abs() function returns the absolute (positive) value of the specified number:
  print(x) #7.25

